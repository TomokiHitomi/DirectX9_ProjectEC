//=============================================================================
// コンストラクタ（初期化処理）
//=============================================================================
TitleMenu::TitleMenu(void)
{
	LPDIRECT3DDEVICE9 pDevice = GetDevice();

	// 設定データの読み込み
	//Load();

	// 各種ポインタを NULL で初期化
	m_pTex = NULL;
	m_pTex = new CXTexture;

	m_pTex->Init(
		pDevice,						// デバイス
		(LPSTR)TITLEUI_MENU_TEX_PATH,	// テクスチャパス
		(LPSTR)TITLEUI_MENU_FILE_PATH,	// ファイルパス
		TITLEUI_MENU_MAX,				// 最大数
		1, 1							// テクスチャ分割数（X,Y）
	);
}

//=============================================================================
// デストラクタ（終了処理）
//=============================================================================
TitleMenu::~TitleMenu(void)
{
	Release();
}

//=============================================================================
// 解放処理
//=============================================================================
void TitleMenu::Release(void)
{
	SAFE_RELEASE(m_pTex);
	SAFE_DELETE(m_pTex);
}

//=============================================================================
// 更新処理
//=============================================================================
void TitleMenu::Update(void)
{
#ifdef _DEBUG
	ImGui::SetNextTreeNodeOpen(false, ImGuiSetCond_Once);
	bool bGui = ImGui::TreeNode("TitleMenu");
	if (bGui)
	{
		ImGui::TreePop();
	}
#endif

	SAFE_UPDATE(m_pTex);
}

//=============================================================================
// 描画処理
//=============================================================================
void TitleMenu::Draw(void)
{
	SAFE_DRAW(m_pTex);
}