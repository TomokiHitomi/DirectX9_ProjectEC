class File
{
public:
	// コンストラクタ（初期化処理）
	File(void);
	//デストラクタ（終了処理）
	~File(void);

	static void PD3DXVECTOR3(FILE* fp, D3DXVECTOR3 vVec)
	{
		fprintf_s(fp, "%f%f%f", vVec.x, vVec.y, vVec.z);
	}
	static D3DXVECTOR3 SD3DXVECTOR3(FILE* fp)
	{
		D3DXVECTOR3 temp;
		fscanf_s(fp, "%f%f%f", temp.x, temp.y, temp.z);
		return temp;
	}
};

