//=============================================================================
// コンストラクタ（初期化処理）
//=============================================================================
TitleLogo::TitleLogo(void)
{
	LPDIRECT3DDEVICE9 pDevice = GetDevice();

	// 設定データの読み込み
	//Load();

	// 各種ポインタを NULL で初期化
	m_pTex = NULL;
	m_pTex = new CXTexture;

	m_pTex->Init(
		pDevice,						// デバイス
		(LPSTR)TITLEUI_LOGO_TEX_PATH, 	// テクスチャパス
		(LPSTR)TITLEUI_LOGO_FILE_PATH, 	// ファイルパス
		TITLEUI_LOGO_MAX, 				// 最大数
		1, 1							// テクスチャ分割数（X,Y）
	);
}

//=============================================================================
// デストラクタ（終了処理）
//=============================================================================
TitleLogo::~TitleLogo(void)
{
	Release();
}

//=============================================================================
// 解放処理
//=============================================================================
void TitleLogo::Release(void)
{
	SAFE_RELEASE(m_pTex);
	SAFE_DELETE(m_pTex);
}

//=============================================================================
// 更新処理
//=============================================================================
void TitleLogo::Update(void)
{
#ifdef _DEBUG
	ImGui::SetNextTreeNodeOpen(false, ImGuiSetCond_Once);
	bool bGui = ImGui::TreeNode("TitleLogo");
	if (bGui)
	{
		ImGui::TreePop();
	}
#endif

	SAFE_UPDATE(m_pTex);
}

//=============================================================================
// 描画処理
//=============================================================================
void TitleLogo::Draw(void)
{
	SAFE_DRAW(m_pTex);
}